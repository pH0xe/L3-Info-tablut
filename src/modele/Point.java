package modele;

public class Point {
    private int l;
    private int c;


    public Point(int l, int c){
        this.l = l;
        this.c = c;
    }

    public int getL(){
        return l;
    }

    public int getC(){
        return c;
    }

    public void setL(int l){
        this.l = l;
    }

    public void setC(int c){
        this.c = c ;
    }

}
